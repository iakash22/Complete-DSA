#include<iostream>
using namespace std;
int main()
{
    int a;
    cout<<"integer size "<<sizeof(a)<<endl;
    char c;
    cout<<"character size "<<sizeof(c)<<endl;
    float f;
    cout<<"float size "<<sizeof(f)<<endl;
    long l;
    cout<<"long size "<<sizeof(l)<<endl;
    double d;
    cout<<"double size "<<sizeof(d)<<endl;
    bool b;
    cout<<"boolean size "<<sizeof(b)<<endl;
    return 0;

}